int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	while (i * i != nb || i > nb || nb < 0)
	{
		if (i * i == nb)
			break ;
		if (i > nb || nb < 0)
			break ;
		i++;
	}
	if (i * i == nb)
		return (i);
	else
		return (0);
}
